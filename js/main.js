let epic = document.querySelector('body')

epic.addEventListener('keydown', (event) => {
    if (event.key === "w") {
        console.log("joe biden")
    } if (event.key === "a") {
        console.log("jee")
    } if (event.key === "s") {
        console.log("jaa")
    } if (event.key === "d") {
        console.log("jaf")
    }
});

